	"chat_completion_time_ms" int4,
	"summary_ids_in_context" _int4,
	"chunk_ids_in_context" _int4
);

CREATE OR REPLACE FUNCTION generate_short_id () returns trigger AS $$
BEGIN
  NEW.short_id := id_encode(NEW.id);
  RETURN NEW;
END;
$$ language plpgsql;

CREATE OR REPLACE TRIGGER trigger_on_insert_user_requests before insert ON user_requests FOR each ROW
EXECUTE function generate_short_id ();
