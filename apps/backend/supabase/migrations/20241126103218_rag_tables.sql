CREATE EXTENSION if NOT EXISTS "vector"
WITH
	schema "extensions";

CREATE EXTENSION if NOT EXISTS "pg_jsonschema"
WITH
	schema "extensions";

CREATE TABLE "public"."registered_documents" (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	owned_by_user_id UUID DEFAULT NULL,
	"source_url" TEXT NOT NULL,
	"source_type" TEXT NOT NULL,
	"registered_at" TIMESTAMPTZ NOT NULL,
	"metadata" JSONB
);

CREATE TABLE "public"."processed_documents" (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	owned_by_user_id UUID DEFAULT NULL,
	"file_checksum" TEXT NOT NULL,
	"file_size" int4 NOT NULL,
	"num_pages" int4 NOT NULL,
	"processing_started_at" TIMESTAMPTZ,
	"processing_finished_at" TIMESTAMPTZ,
	"processing_error" TEXT,
	"registered_document_id" int4,
	CONSTRAINT "processed_documents_registered_document_id_fkey" FOREIGN key ("registered_document_id") REFERENCES "public"."registered_documents" ("id") ON DELETE CASCADE
);

CREATE TABLE "public"."processed_document_summaries" (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	owned_by_user_id UUID DEFAULT NULL,
	"summary" TEXT NOT NULL,
	"summary_embedding_temp" vector,
	"tags" _text NOT NULL,
	"processed_document_id" int4,
	"summary_embedding" vector NOT NULL,
	"summary_temp" TEXT,
	"tags_temp" _text,
	CONSTRAINT "processed_document_summaries_processed_document_id_fkey" FOREIGN key ("processed_document_id") REFERENCES "public"."processed_documents" ("id") ON DELETE CASCADE
);

CREATE TABLE "public"."processed_document_chunks" (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	owned_by_user_id UUID DEFAULT NULL,
	"content" TEXT NOT NULL,
	"embedding_temp" vector,
	"page" int4 NOT NULL,
	"chunk_index" int4 NOT NULL,
	"processed_document_id" int4,
	"embedding" vector NOT NULL,
	"content_temp" TEXT,
	CONSTRAINT "processed_document_chunks_processed_document_id_fkey" FOREIGN key ("processed_document_id") REFERENCES "public"."processed_documents" ("id") ON DELETE CASCADE
);

