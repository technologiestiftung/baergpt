	"properties": {
		"query": {
			"type": "string"
		},
		"match_threshold": {
			"type": "number"
		},
		"num_probes_chunks": {
			"type": "number"
		},
		"num_probes_summaries": {
			"type": "number"
		},
		"search_algorithm": {
			"type": "string"
		},
		"chunk_limit": {
			"type": "number"
		},
		"summary_limit": {
			"type": "number"
		},
		"document_limit": {
			"type": "number"
		}
	}
}'::JSON,
			request_payload
		)
	),
	"question" TEXT NOT NULL,
	"generated_answer" TEXT,
	"error" TEXT,
